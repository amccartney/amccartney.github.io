import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const DATA_FILES = [
	"src/lib/data/projects.json",
	"src/lib/data/fine-art-projects.json"
];

const OUTPUT_FILE = "src/lib/data/projectImageDimensions.js";

const JPEG_MARKERS = new Set([
	0xc0, 0xc1, 0xc2, 0xc3,
	0xc5, 0xc6, 0xc7,
	0xc9, 0xca, 0xcb,
	0xcd, 0xce, 0xcf
]);

function getDefaultRootDir() {
	return path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
}

function toPosixPath(filePath) {
	return filePath.split(path.sep).join("/");
}

async function readProjectImagePaths(rootDir) {
	const imagePaths = new Set();

	for (const relativePath of DATA_FILES) {
		const absolutePath = path.join(rootDir, relativePath);
		const fileContents = await readFile(absolutePath, "utf8");
		const records = JSON.parse(fileContents);

		for (const record of records) {
			if (typeof record.image === "string" && record.image.startsWith("/")) {
				imagePaths.add(record.image);
			}
		}
	}

	return [...imagePaths].sort();
}

function parsePng(buffer) {
	return {
		width: buffer.readUInt32BE(16),
		height: buffer.readUInt32BE(20)
	};
}

function parseGif(buffer) {
	return {
		width: buffer.readUInt16LE(6),
		height: buffer.readUInt16LE(8)
	};
}

function parseJpeg(buffer) {
	let offset = 2;

	while (offset < buffer.length) {
		while (buffer[offset] === 0xff) {
			offset += 1;
		}

		const marker = buffer[offset];
		offset += 1;

		if (marker === 0xd8 || marker === 0xd9) {
			continue;
		}

		if (marker === 0x01 || (marker >= 0xd0 && marker <= 0xd7)) {
			continue;
		}

		const segmentLength = buffer.readUInt16BE(offset);

		if (segmentLength < 2) {
			throw new Error("Invalid JPEG segment length");
		}

		if (JPEG_MARKERS.has(marker)) {
			return {
				height: buffer.readUInt16BE(offset + 3),
				width: buffer.readUInt16BE(offset + 5)
			};
		}

		offset += segmentLength;
	}

	throw new Error("Unsupported JPEG format");
}

function parseSvg(buffer) {
	const svg = buffer.toString("utf8");
	const widthMatch = svg.match(/\bwidth=["']([\d.]+)(px)?["']/i);
	const heightMatch = svg.match(/\bheight=["']([\d.]+)(px)?["']/i);

	if (widthMatch && heightMatch) {
		return {
			width: Math.round(Number(widthMatch[1])),
			height: Math.round(Number(heightMatch[1]))
		};
	}

	const viewBoxMatch = svg.match(/\bviewBox=["']([^"']+)["']/i);

	if (viewBoxMatch) {
		const parts = viewBoxMatch[1]
			.trim()
			.split(/[\s,]+/)
			.map((value) => Number(value));

		if (parts.length === 4 && Number.isFinite(parts[2]) && Number.isFinite(parts[3])) {
			return {
				width: Math.round(parts[2]),
				height: Math.round(parts[3])
			};
		}
	}

	throw new Error("SVG is missing width/height information");
}

function parseWebp(buffer) {
	const chunkType = buffer.toString("ascii", 12, 16);

	if (chunkType === "VP8X") {
		return {
			width: 1 + buffer.readUIntLE(24, 3),
			height: 1 + buffer.readUIntLE(27, 3)
		};
	}

	if (chunkType === "VP8L") {
		const b0 = buffer[21];
		const b1 = buffer[22];
		const b2 = buffer[23];
		const b3 = buffer[24];

		return {
			width: 1 + (((b1 & 0x3f) << 8) | b0),
			height: 1 + (((b3 & 0x0f) << 10) | (b2 << 2) | ((b1 & 0xc0) >> 6))
		};
	}

	throw new Error(`Unsupported WEBP chunk type: ${chunkType}`);
}

function getImageDimensions(filePath, buffer) {
	const extension = path.extname(filePath).toLowerCase();

	switch (extension) {
		case ".png":
			return parsePng(buffer);
		case ".gif":
			return parseGif(buffer);
		case ".jpg":
		case ".jpeg":
			return parseJpeg(buffer);
		case ".svg":
			return parseSvg(buffer);
		case ".webp":
			return parseWebp(buffer);
		default:
			throw new Error(`Unsupported image format: ${extension}`);
	}
}

function buildModuleSource(dimensions) {
	const lines = [
		"// This file is auto-generated by scripts/generate-project-image-dimensions.mjs.",
		"const projectImageDimensions = {"
	];

	for (const [imagePath, { width, height }] of Object.entries(dimensions)) {
		lines.push(`\t"${imagePath}": { width: ${width}, height: ${height} },`);
	}

	lines.push("};", "", "export default projectImageDimensions;", "");

	return lines.join("\n");
}

export async function generateProjectImageDimensions({ rootDir = getDefaultRootDir() } = {}) {
	const imagePaths = await readProjectImagePaths(rootDir);
	const dimensions = {};
	const warnings = [];

	for (const imagePath of imagePaths) {
		const absoluteImagePath = path.join(rootDir, "static", imagePath.slice(1));

		try {
			const buffer = await readFile(absoluteImagePath);
			dimensions[imagePath] = getImageDimensions(absoluteImagePath, buffer);
		} catch (error) {
			warnings.push(`${imagePath}: ${error.message}`);
		}
	}

	const outputPath = path.join(rootDir, OUTPUT_FILE);
	const nextSource = buildModuleSource(dimensions);

	/** @type {string | null} */
	let previousSource = null;

	try {
		previousSource = await readFile(outputPath, "utf8");
	} catch {
		previousSource = null;
	}

	const changed = previousSource !== nextSource;

	if (changed) {
		await writeFile(outputPath, nextSource, "utf8");
	}

	return {
		changed,
		count: Object.keys(dimensions).length,
		outputPath: toPosixPath(path.relative(rootDir, outputPath)),
		warnings
	};
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
	const result = await generateProjectImageDimensions();

	for (const warning of result.warnings) {
		console.warn(`[project-image-dimensions] Skipped ${warning}`);
	}

	console.log(
		`[project-image-dimensions] ${result.changed ? "Updated" : "Verified"} ${result.outputPath} with ${result.count} image entr${result.count === 1 ? "y" : "ies"}.`
	);
}
